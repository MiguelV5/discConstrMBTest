import os
import discord


client = discord.Client()

@client.event
async def on_ready():
  print('Logged in as {0.user}'.format(client))

@client.event
async def on_message(message):
  if message.author == client.user:
    return
  
  msg = message.content

  if msg.startswith('#trigger'):
    await message.channel.send('Si')


client.run(os.environ['TOKEN'])
